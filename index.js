//Comparison Operators........................
//(==)operator...................

const num1 = "20";
const num2 = 20;
let res = num1 == num2;
console.log(res); //True

const num3 = 20;
const num4 = 30;
let result = num3 == num4;
console.log(result); //false


//(===)operator......................

const num5 = "20";
const num6 = "20";
let res1 = num5 === num6;
console.log(res1); //True

const num7 = "20";
const num8 = "30";
let res2 = num7 === num8;
console.log(res2); //false

//(!=)operator..............

let x = "20";
let y = true;
let res3  = x != y;
console.log(res3); //true

let h = 20;
let c = "20";
let res4 = h != c;
console.log(res4); 

//(!==)operator............

let q = "20";
let e = 20;
let res5 = q !== e;
console.log(res5);


